-module(chain).

-export([start/0, stop/0]). %% external

%% ====================================================================
%% External functions
%% ====================================================================

%%--------------------------------------------------------------------
%% Function: start() -> {ok, Pid} | {ok, Pid, State} | {error, Reason}
%% Description: Start chain application  
%%--------------------------------------------------------------------
start() ->
    application:start(chain, permanent).

%%--------------------------------------------------------------------
%% Function: stop() -> ok
%% Description: Stop chain application  
%%--------------------------------------------------------------------
stop() ->
    application:stop(chain).

